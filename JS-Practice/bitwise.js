// Bitwise operators and , or , not, xor, left shift, right shift
//  & - and , | - or , ~ - not, ^ - xor, >> - signed right shift , << zero fill left shift, >>> - zero fill right shift

let x = 5 & 1;
console.log(x);

let y = ~5;
console.log(y);

let z = 5 | 1;
console.log(z);

let p = 5 ^ 6;
console.log(p);

let q = 5 << 1;
console.log(q);

let r = -6 >> 1;
console.log(r);

let s = 7 >>> 1;
console.log(s);
